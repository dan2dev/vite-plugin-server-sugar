import { existsSync } from "node:fs";
import { Registry } from "../core/registry";
import type { BackendEntry } from "../types";
import { API_PREFIX } from "../constants";
import { toImportPath } from "../utils/path";
import { backendConstName } from "../utils/crypto";
import { runtimeImportSpecifier } from "../dev-server/virtual-modules";

interface SpecifierBindings {
  default?: string;
  namespace?: string;
  named: Map<string, string>;
}

function importedNameToken(imported: string): string {
  return /^[A-Za-z_$][\w$]*$/.test(imported)
    ? imported
    : JSON.stringify(imported);
}

export function generateBundleContent(
  registry: Registry,
  serverEntry: string | undefined,
  serverEntryPath: string | null,
  serverOutDir: string,
  clientOutDir: string,
  port: number,
): string | null {
  const hasServerEntry = serverEntryPath ? existsSync(serverEntryPath) : false;

  if (serverEntry && !hasServerEntry) {
    throw new Error(
      `[server-build] serverEntry '${serverEntry}' was configured but the file does not exist.`,
    );
  }

  if (!hasServerEntry && registry.size === 0) return null;

  const serverImportPath = hasServerEntry
    ? toImportPath(serverOutDir, serverEntryPath!)
    : null;
  const clientRootRelativePath = toImportPath(serverOutDir, clientOutDir);

  // Backend handlers from many source files are inlined into this single
  // module. Two files may legitimately use the same local name for different
  // imports, which would otherwise collide as duplicate top-level bindings.
  // Every distinct import gets a unique top-level alias and each handler
  // re-binds its own original local names through a small factory wrapper.
  const aliasByKey = new Map<string, string>();
  const bindingsBySpecifier = new Map<string, SpecifierBindings>();
  const factoryArgsByEndpoint = new Map<
    string,
    { locals: string[]; aliases: string[] }
  >();
  let aliasCount = 0;

  const aliasFor = (key: string): string => {
    let alias = aliasByKey.get(key);
    if (!alias) {
      alias = `__dep_${aliasCount++}`;
      aliasByKey.set(key, alias);
    }
    return alias;
  };

  const bindingsFor = (specifier: string): SpecifierBindings => {
    let bindings = bindingsBySpecifier.get(specifier);
    if (!bindings) {
      bindings = { named: new Map() };
      bindingsBySpecifier.set(specifier, bindings);
    }
    return bindings;
  };

  for (const entry of registry.values()) {
    const locals: string[] = [];
    const aliases: string[] = [];

    for (const runtimeImport of entry.imports) {
      const specifier = runtimeImportSpecifier(
        entry.file,
        runtimeImport.specifier,
        serverOutDir,
      );
      const bindings = bindingsFor(specifier);

      if (runtimeImport.defaultName) {
        const alias = aliasFor(JSON.stringify(["default", specifier]));
        bindings.default = alias;
        locals.push(runtimeImport.defaultName);
        aliases.push(alias);
      }
      if (runtimeImport.namespaceName) {
        const alias = aliasFor(JSON.stringify(["namespace", specifier]));
        bindings.namespace = alias;
        locals.push(runtimeImport.namespaceName);
        aliases.push(alias);
      }
      for (const { imported, local } of runtimeImport.named) {
        const alias = aliasFor(JSON.stringify(["named", specifier, imported]));
        bindings.named.set(imported, alias);
        locals.push(local);
        aliases.push(alias);
      }
    }

    factoryArgsByEndpoint.set(entry.endpoint, { locals, aliases });
  }

  const backendImports: string[] = [];
  for (const [specifier, bindings] of bindingsBySpecifier) {
    const quoted = JSON.stringify(specifier);
    if (bindings.default) {
      backendImports.push(`import ${bindings.default} from ${quoted};`);
    }
    if (bindings.namespace) {
      backendImports.push(`import * as ${bindings.namespace} from ${quoted};`);
    }
    if (bindings.named.size > 0) {
      const items = [...bindings.named]
        .map(
          ([imported, alias]) => `${importedNameToken(imported)} as ${alias}`,
        )
        .join(", ");
      backendImports.push(`import { ${items} } from ${quoted};`);
    }
  }

  const lines: string[] = [
    "// Auto-generated by vite-plugin-server-build - do not edit",
    serverImportPath
      ? `import * as __serverEntry from '${serverImportPath}';`
      : "import { Hono } from 'hono';",
    "import { dirname, isAbsolute, join, relative, resolve } from 'path';",
    "import { fileURLToPath } from 'url';",
    ...backendImports,
    "",
  ];

  if (serverImportPath) {
    lines.push("const app = __serverEntry.default ?? __serverEntry.app;", "");
  } else {
    lines.push("const app = new Hono();", "");
  }

  if (serverImportPath) {
    lines.push(
      "if (!app || typeof app.fetch !== 'function') {",
      `  throw new Error("[server-build] ${serverEntry} must export a Hono app as default export or named 'app' export.");`,
      "}",
      "",
    );
  }

  // Group entries by source file so module-level declarations (e.g. `const
  // state = {}`) are shared across all handlers from the same file.
  const entriesByFile = new Map<string, BackendEntry[]>();
  for (const entry of registry.values()) {
    const arr = entriesByFile.get(entry.file) ?? [];
    arr.push(entry);
    entriesByFile.set(entry.file, arr);
  }

  for (const [, fileEntries] of entriesByFile) {
    const moduleDeclsJs = fileEntries[0]?.moduleDeclsJs ?? '';
    const hasSiblingCrossRefs = fileEntries[0]?.hasSiblingCrossRefs ?? false;
    const useIIFE = !!moduleDeclsJs || hasSiblingCrossRefs;

    if (!useIIFE) {
      // No module-level state and no sibling cross-refs: emit each handler individually.
      for (const entry of fileEntries) {
        const constName = backendConstName(entry.endpoint);
        const { locals, aliases } = factoryArgsByEndpoint.get(entry.endpoint)!;

        if (locals.length === 0) {
          lines.push(`const ${constName} = ${entry.fnJs};`, "");
        } else {
          lines.push(
            `const ${constName} = ((${locals.join(", ")}) => (${entry.fnJs}))(${aliases.join(", ")});`,
            "",
          );
        }
      }
    } else {
      // Wrap all handlers from this file in an IIFE so they share the same
      // module-level state and can reference each other by their original names.
      const constNames = fileEntries.map((e) => backendConstName(e.endpoint));
      lines.push(`const { ${constNames.join(", ")} } = (() => {`);

      if (moduleDeclsJs) {
        for (const declLine of moduleDeclsJs.split("\n")) {
          lines.push(`  ${declLine}`);
        }
      }

      // Declare each handler as a named local so siblings can call each other.
      for (const entry of fileEntries) {
        const constName = backendConstName(entry.endpoint);
        const localName = entry.originalName ?? constName;
        const { locals, aliases } = factoryArgsByEndpoint.get(entry.endpoint)!;
        const handlerExpr =
          locals.length === 0
            ? entry.fnJs
            : `((${locals.join(", ")}) => (${entry.fnJs}))(${aliases.join(", ")})`;
        lines.push(`  const ${localName} = ${handlerExpr};`);
      }

      lines.push("  return {");

      for (const entry of fileEntries) {
        const constName = backendConstName(entry.endpoint);
        const localName = entry.originalName ?? constName;
        lines.push(`    ${constName}: ${localName},`);
      }

      lines.push("  };", "})();", "");
    }
  }

  lines.push("const __backendHandlers = {");
  for (const entry of registry.values()) {
    lines.push(
      `  ${JSON.stringify(entry.endpoint)}: ${backendConstName(entry.endpoint)},`,
    );
  }
  lines.push("};", "");

  lines.push(
    `app.post('${API_PREFIX}*', async (c) => {`,
    "  const url = new URL(c.req.url);",
    "  let endpoint;",
    "  try {",
    `    endpoint = decodeURIComponent(url.pathname.slice(${API_PREFIX.length}));`,
    "  } catch {",
    '    return c.json({ error: "Bad request" }, 400);',
    "  }",
    "  const handler = __backendHandlers[endpoint];",
    "",
    "  if (!handler) {",
    "    return c.json({ error: `Handler not found for endpoint: ${endpoint}` }, 404);",
    "  }",
    "",
    '  const contentType = c.req.header("content-type");',
    '  if (contentType && !contentType.toLowerCase().includes("application/json")) {',
    '    return c.json({ error: "Unsupported media type. Expected application/json" }, 415);',
    "  }",
    "",
    "  let payload;",
    "  try {",
    "    const rawBody = await c.req.text();",
    "    const trimmedBody = rawBody.trim();",
    "    payload = trimmedBody.length > 0 ? JSON.parse(trimmedBody) : [];",
    "  } catch {",
    '    return c.json({ error: "Invalid JSON payload" }, 400);',
    "  }",
    "",
    "  try {",
    "    const args = Array.isArray(payload) ? payload : [payload];",
    "    const result = await handler(...args);",
    "    if (result === undefined) return c.body(null, 204);",
    "    return c.json(result);",
    "  } catch (e) {",
    "    const message = e instanceof Error ? e.message : String(e);",
    "    console.error(`[backend] Error in ${endpoint}:`, e);",
    "    return c.json({ error: message }, 500);",
    "  }",
    "});",
    "",
    "// Backend endpoints only accept POST; reject other methods explicitly",
    `app.all('${API_PREFIX}*', (c) => c.json({ error: 'Method not allowed' }, 405, { Allow: 'POST' }));`,
    "",
    "// Serve static assets from the client build directory",
    "const __moduleUrl = import.meta.url;",
    'const __serverRoot = __moduleUrl.startsWith("file:") && !__moduleUrl.includes("$bunfs")',
    "  ? dirname(fileURLToPath(__moduleUrl))",
    "  : dirname(process.execPath);",
    `const __clientRoot = resolve(__serverRoot, ${JSON.stringify(clientRootRelativePath)});`,
    "",
    'app.use("*", async (c, next) => {',
    '  if (c.req.method !== "GET" && c.req.method !== "HEAD") {',
    "    return next();",
    "  }",
    "",
    "  const url = new URL(c.req.url);",
    "  let pathname;",
    "  try {",
    "    pathname = decodeURIComponent(url.pathname);",
    "  } catch {",
    '    return c.text("Bad request", 400);',
    "  }",
    '  const normalizedPathname = pathname.replace(/\\/+/g, "/");',
    '  const safePathname = normalizedPathname.startsWith("/") ? normalizedPathname : `/${normalizedPathname}`;',
    '  if (safePathname.includes("\\0")) {',
    '    return c.text("Bad request", 400);',
    "  }",
    "  const resolvedClientRoot = resolve(__clientRoot);",
    "  const filePath = resolve(resolvedClientRoot, `.${safePathname}`);",
    "  const relativePath = relative(resolvedClientRoot, filePath);",
    "",
    "  // Security: prevent directory traversal outside of the client build.",
    "  if (",
    '    relativePath === ".." ||',
    '    relativePath.startsWith("../") ||',
    '    relativePath.startsWith("..\\\\") ||',
    "    isAbsolute(relativePath)",
    "  ) {",
    '    return c.text("Not found", 404);',
    "  }",
    "",
    "  // Try to serve the exact file",
    '  if (!safePathname.endsWith("/")) {',
    "    const file = Bun.file(filePath);",
    "    if (await file.exists()) {",
    '      const cacheControl = safePathname.includes("/assets/")',
    '        ? "public, max-age=31536000, immutable"',
    '        : "public, max-age=0, must-revalidate";',
    '      return new Response(file, { headers: { "Cache-Control": cacheControl } });',
    "    }",
    "  }",
    "",
    "  return next();",
    "});",
    "",
    "// SPA fallback: serve index.html for any other GET requests",
    'app.get("*", async (c) => {',
    '  const index = Bun.file(join(__clientRoot, "index.html"));',
    "  if (await index.exists()) {",
    "    return new Response(index, {",
    '      headers: { "Content-Type": "text/html; charset=utf-8", "Cache-Control": "public, max-age=0, must-revalidate" },',
    "    });",
    "  }",
    "",
    '  return c.text("Not found", 404);',
    "});",
    "",
    "const __rawPort = Bun.env.PORT;",
    "const __parsedPort = __rawPort ? Number(__rawPort) : NaN;",
    `const PORT = Number.isInteger(__parsedPort) && __parsedPort >= 1 && __parsedPort <= 65535 ? __parsedPort : ${port};`,
    `if (__rawPort && PORT !== __parsedPort) console.warn(\`[backend] Invalid PORT value "\${__rawPort}", falling back to ${port}\`);`,
    "",
    "Bun.serve({",
    "  port: PORT,",
    "  fetch: (req) => app.fetch(req),",
    "});",
    "",
    "console.log(`[backend] Listening on http://localhost:${PORT}`);",
  );

  return lines.join("\n");
}
